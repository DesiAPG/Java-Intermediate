public class ArrayManagement {
    public static void main(String[] args) {

        String array[] = new String[4];

        array[0] = "Duvan";
        array[1] = "Camilo";
        array[2] = "John";
        array[3] = "David";

        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}
